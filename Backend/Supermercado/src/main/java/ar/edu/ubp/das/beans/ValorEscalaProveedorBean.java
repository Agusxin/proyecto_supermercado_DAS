package ar.edu.ubp.das.beans;

public class ValorEscalaProveedorBean {
	
	private int valor_escala;
	private String descripcion_valor;
	private int valor_ponderado;
	
	
	public int getValor_escala() {
		return valor_escala;
	}
	public void setValor_escala(int valor_escala) {
		this.valor_escala = valor_escala;
	}
	
	
	public String getDescripcion_valor() {
		return descripcion_valor;
	}
	public void setDescripcion_valor(String descripcion_valor) {
		this.descripcion_valor = descripcion_valor;
	}
	
	
	public int getValor_ponderado() {
		return valor_ponderado;
	}
	public void setValor_ponderado(int valor_ponderado) {
		this.valor_ponderado = valor_ponderado;
	}
	
	
	
	

}
